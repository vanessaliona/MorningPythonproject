# Arithmetic operations
num1 = 10
num2 = 3
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) # Modulus - Return the remainder after division

# Comparison operators - Compares value
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) # Equal to
print(num1 != num2) # Not equal to

# Assignment operation - Assign values to variables
x= 100
print(x)

x += 2
print(x)

x -= 3
print(x)

